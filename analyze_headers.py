def analyze_header_structure():
    """ヘッダー構造を詳細に分析"""
    headers = create_jinjer_headers()
    
    print(f"総ヘッダー数: {len(headers)}")
    print("\n=== ヘッダー構造詳細 ===")
    
    # 各セクションの開始位置を特定
    current_index = 0
    sections = [
        ('基本情報', 5),
        ('スケジュール情報', 13),
        ('休日設定', 1),
        ('実際の出退勤時刻', 20),
        ('実際の休憩時刻', 20),
        ('食事時間', 4),
        ('外出・再入', 20),
        ('休日休暇', 10),
        ('管理情報', 7),
        ('直行・直帰', 20),
        ('打刻区分ID', 50),
        ('勤務状況フラグ', 5),
        ('労働時間計算', 13),
        ('乖離時間', 4)
    ]
    
    for section_name, count in sections:
        start = current_index
        end = current_index + count
        print(f"\n{section_name}: インデックス {start}-{end-1} ({count}列)")
        for i in range(start, min(end, len(headers))):
            print(f"  [{i:3d}] {headers[i]}")
        current_index = end
    
    # 重要なインデックスを確認
    print("\n=== 重要なインデックス ===")
    key_indices = {
        '出勤1': 21,
        '退勤1': 22,
        '打刻区分ID:1': 122,
        '打刻区分ID:50': 171,
        '未打刻': 172,
        '実績確定状況': 176,
        '総労働時間': 177,
        '申請承認済法定外残業時間': 189
    }
    
    for key, expected_index in key_indices.items():
        if expected_index < len(headers):
            actual_header = headers[expected_index]
            print(f"  インデックス {expected_index:3d}: 期待='{key}', 実際='{actual_header}'")
            if key not in actual_header and actual_header not in key:
                print(f"    ⚠️  不一致！")
        else:
            print(f"  インデックス {expected_index:3d}: 期待='{key}', 実際=範囲外")
    
    # 現在のコードで使用されているインデックスをチェック
    print("\n=== 現在のコードのインデックス問題 ===")
    problematic_indices = {
        '打刻区分ID開始位置（現在のコード）': 122,
        '勤務状況フラグ開始位置（現在のコード）': 172,
        '労働時間計算開始位置（現在のコード）': 177
    }
    
    for desc, index in problematic_indices.items():
        if index < len(headers):
            print(f"  {desc}: インデックス {index} = '{headers[index]}'")
        else:
            print(f"  {desc}: インデックス {index} = 範囲外")

if __name__ == "__main__":
    analyze_header_structure()