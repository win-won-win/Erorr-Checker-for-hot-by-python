def analyze_correct_structure():
    """正しい133列構造を分析"""
    headers = create_correct_jinjer_headers()
    
    print(f"総ヘッダー数: {len(headers)}")
    print("\n=== 正しい133列構造 ===")
    
    sections = [
        ('基本情報', 5),
        ('スケジュール情報', 15),
        ('休日設定', 1),
        ('実際の出退勤時刻', 20),
        ('実際の休憩時刻', 20),
        ('食事時間', 4),
        ('外出・再入', 20),
        ('休日休暇', 10),
        ('管理情報', 7),
        ('直行・直帰', 20),
        ('打刻区分ID', 10),
        ('勤務状況フラグ', 5),
        ('労働時間計算', 6)
    ]
    
    current_index = 0
    for section_name, count in sections:
        start = current_index
        end = current_index + count
        print(f"\n{section_name}: インデックス {start}-{end-1} ({count}列)")
        for i in range(start, min(end, len(headers))):
            print(f"  [{i:3d}] {headers[i]}")
        current_index = end
    
    # 重要なインデックスを確認
    print("\n=== 重要なインデックス（133列版） ===")
    key_indices = {
        '出勤1': 21,
        '退勤1': 22,
        '打刻区分ID:1': 122,
        '打刻区分ID:10': 131,
        '未打刻': 132,
        '実績確定状況': 136,
        '総労働時間': 137,
        '深夜時間': 142
    }
    
    for key, expected_index in key_indices.items():
        if expected_index < len(headers):
            actual_header = headers[expected_index]
            print(f"  インデックス {expected_index:3d}: 期待='{key}', 実際='{actual_header}'")
            if key not in actual_header and actual_header not in key:
                print(f"    ⚠️  不一致！")
        else:
            print(f"  インデックス {expected_index:3d}: 期待='{key}', 実際=範囲外")

if __name__ == "__main__":
    analyze_correct_structure()